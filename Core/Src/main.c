void encoder_out(uint32_t outFreq, uint32_t outTicks, uint8_t direction) {
	uint32_t timerFreq = HAL_RCC_GetHCLKFreq() / 4;
	LL_TIM_SetAutoReload(TIM5, __LL_TIM_CALC_PSC(timerFreq, outFreq));
	LL_TIM_GenerateEvent_UPDATE(TIM5);

	LL_TIM_GenerateEvent_UPDATE(TIM3);

	LL_TIM_SetAutoReload(TIM2, outTicks - 1);
	LL_TIM_GenerateEvent_UPDATE(TIM2);

	LL_TIM_EnableAllOutputs(TIM1);
	LL_TIM_CC_EnableChannel(TIM1, LL_TIM_CHANNEL_CH1 | LL_TIM_CHANNEL_CH2);
	LL_TIM_SetCounterMode(TIM1, direction ? LL_TIM_COUNTERMODE_DOWN : LL_TIM_COUNTERMODE_UP);

	LL_TIM_EnableCounter(TIM1);
	LL_TIM_EnableCounter(TIM2);
	LL_TIM_EnableCounter(TIM3);
	LL_TIM_EnableCounter(TIM5);

	while (LL_TIM_IsEnabledCounter(TIM2));

	LL_TIM_DisableCounter(TIM5);
	LL_TIM_DisableCounter(TIM3);
	LL_TIM_DisableCounter(TIM1);

}


